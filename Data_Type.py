#Getting the Data Type
x = 200
print(type(x))

#Setting the Data Type
#String
x = "Hello World"
print(x)
print(type(x))

#Int
a = 30
print(a)
print(type(a))

#Float
b = 805.69
print(b)
print(type(b))

#Complex
c = 1j
print(c)
print(type(c))

#List
d = ["Rombo", "Triangulo", "Circulo", "Cuadrado"]
print(d)
print(type(d))

#Tuple
e = ("Rombo", "Triangulo", "Circulo", "Cuadrado")
print(e)
print(type(e))

#Range
f = range(8)
print(f)
print(type(f))

#Dict
g = {"Name" : "Alejo", "age" : 36}
print(g)
print(type(g))

#Set
h = {"Manzana", "Banana", "Cereza"}
print(h)
print(type(h))

#Frozenset
i = frozenset({"Apple", "Banana", "Cherry"})
print(i)
print(type(i))

#Bool
j = True
print(j)
print(type(j))

#Bytes
k = b"Hello"
print(k)
print(type(k))

#Bytearray
l = bytearray(6)
print(l)
print(type(l))

#Memoryview
m = memoryview(bytes(6))
print(m)
print(type(m))

#NoneType
